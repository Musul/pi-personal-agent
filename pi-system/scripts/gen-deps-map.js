#!/usr/bin/env node
/**
 * gen-deps-map.js — regenerate ~/pi-system/docs/DEPENDENCIES.md from extension manifests
 *
 * Source of truth:
 *   - ~/pi-system/registry.json
 *   - ~/pi-system/extensions/<name>/manifest.json
 *
 * Output:
 *   - ~/pi-system/docs/DEPENDENCIES.md
 */

import fs from 'node:fs';
import path from 'node:path';
import os from 'node:os';

const HOME = process.env.PI_HOME || os.homedir();
const SYS = path.join(HOME, 'pi-system');
const REG = path.join(SYS, 'registry.json');
const DOCS = path.join(SYS, 'docs');
const OUT = path.join(DOCS, 'DEPENDENCIES.md');

function readJson(p, def) {
  try { return JSON.parse(fs.readFileSync(p, 'utf8')); } catch { return def; }
}

const registry = readJson(REG, { extensions: {} });
const manifests = [];
const eventsProducedBy = {};
const eventsConsumedBy = {};

for (const [name, meta] of Object.entries(registry.extensions || {})) {
  if (!meta.enabled) continue;
  const manifestPath = path.join(SYS, meta.path, 'manifest.json');
  const m = readJson(manifestPath, null);
  if (!m) {
    manifests.push({ name, missing: true, path: meta.path });
    continue;
  }
  manifests.push({ name, ...m, path: meta.path });
  for (const ev of m.emits_events || []) {
    (eventsProducedBy[ev] ||= []).push(name);
  }
  for (const ev of m.consumes_events || []) {
    (eventsConsumedBy[ev] ||= []).push(name);
  }
}

manifests.sort((a, b) => a.name.localeCompare(b.name));

let md = '# DEPENDENCIES — pi-system\n\n';
md += '> Auto-generated by `pi-system/scripts/gen-deps-map.js`. Do not edit by hand.\n';
md += `> Generated: ${new Date().toISOString()}\n\n`;
md += '## Extensions\n\n';

for (const m of manifests) {
  md += `### ${m.name}\n\n`;
  if (m.missing) {
    md += `- ⚠️ manifest.json missing at \`${m.path}\`\n\n`;
    continue;
  }
  md += `- **Type:** ${m.type ?? 'n/a'}\n`;
  md += `- **Version:** ${m.version ?? 'n/a'}\n`;
  if (m.description) md += `- **Description:** ${m.description}\n`;
  if (m.reads?.length) md += `- **Reads:** ${m.reads.map(s => `\`${s}\``).join(', ')}\n`;
  if (m.writes?.length) md += `- **Writes:** ${m.writes.map(s => `\`${s}\``).join(', ')}\n`;
  if (m.emits_events?.length) md += `- **Emits:** ${m.emits_events.map(s => `\`${s}\``).join(', ')}\n`;
  if (m.consumes_events?.length) md += `- **Consumes:** ${m.consumes_events.map(s => `\`${s}\``).join(', ')}\n`;
  if (m.depends_on?.length) md += `- **Depends on:** ${m.depends_on.map(s => `\`${s}\``).join(', ')}\n`;
  if (m.optional_runtime_deps?.length) md += `- **Optional runtime deps:** ${m.optional_runtime_deps.map(s => `\`${s}\``).join(', ')}\n`;
  if (m.env?.length) md += `- **Env vars:** ${m.env.map(s => `\`${s}\``).join(', ')}\n`;
  if (m.system_deps?.length) md += `- **System packages:** ${m.system_deps.map(s => `\`${s}\``).join(', ')}\n`;
  if (m.commands?.length) md += `- **Commands:** ${m.commands.map(s => `\`${s}\``).join(', ')}\n`;
  if (m.user_docs) md += `- **User docs:** \`${m.user_docs}\`\n`;
  md += '\n';
}

md += '## Event graph\n\n';
md += '| Event | Emitted by | Consumed by |\n|---|---|---|\n';
const allEvents = new Set([...Object.keys(eventsProducedBy), ...Object.keys(eventsConsumedBy)]);
for (const ev of [...allEvents].sort()) {
  const prod = (eventsProducedBy[ev] || ['—']).join(', ');
  const cons = (eventsConsumedBy[ev] || ['—']).join(', ');
  md += `| \`${ev}\` | ${prod} | ${cons} |\n`;
}
if (allEvents.size === 0) md += '| (none registered) | | |\n';

md += '\n## Rule of the bus\n\n';
md += 'Extensions MUST NOT import each other directly. All cross-extension communication happens through `pi.events` emit/on. If two extensions need tight coupling, merge them.\n';

if (!fs.existsSync(DOCS)) fs.mkdirSync(DOCS, { recursive: true });
fs.writeFileSync(OUT, md);
console.log(`Wrote ${OUT}`);
